//    the food-web value is really nectar or fruit rather than caterpillars, the
//    pollinator/bird scores carry it and `basis` says so.
//  - size arrays: typical field growth on an average south-Florida site;
//    mature*Ft is the honest eventual ceiling.
//  - other scores (0–100): informed estimates from south-Florida sources — the
