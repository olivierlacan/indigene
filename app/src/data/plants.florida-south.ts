// Seed dataset — south Florida and the Keys, using the greater Miami / lower
// peninsula (USDA zones 10a–11a) as the reference. This is the subtropical
// counterpart to `plants.florida.ts` (north & central Florida): south of the
// ~27.2° N seam — the EPA Southern Florida Coastal Plain ecoregion (76) and the
// warm end of the Southern Coastal Plain (75) — the flora turns tropical, with
// hammock, pineland, and coastal species that won't take a hard freeze. Several
// entries here are frost-tender and belong only in this region.
//
// Numbers and their provenance:
//  - hostLepCount: many south-Florida tropical species host few *documented*
//    Lepidoptera, so counts are honest and low; a handful are keystone butterfly
//    hosts noted specifically (coontie → Atala; corkystem passionvine → zebra
//    longwing, Florida's state butterfly, plus Julia and Gulf fritillary). Where
//    the food-web value is really nectar or fruit rather than caterpillars, the
//    pollinator/bird scores carry it and `basis` says so.
//  - size arrays: typical field growth on an average south-Florida site;
//    mature*Ft is the honest eventual ceiling.
//  - other scores (0–100): informed estimates from south-Florida sources — the
//    Institute for Regional Conservation's "Natives For Your Neighborhood" (IRC),
//    the Atlas of Florida Plants (USF), the Florida Native Plant Society, and
//    UF-IFAS — plus USDA PLANTS. See DATA_SOURCES.md for licensing.
//
